<p class="small">The Occupational Therapy Assistant Program is accredited by the <a href="https://acoteonline.org/">Accreditation Council for Occupational Therapy Education</a> (ACOTE) of the American Occupational Therapy Association (AOTA), located at 6116 Executive Boulevard, Suite 200, North Bethesda, MD 20852-4929. ACOTE's telephone number, c/o AOTA, is (301) 652-AOTA, and its web address is <a href="https://acoteonline.org/">www.acoteonline.org</a>.</p>

<p class="small">Graduates of the program will be eligible to sit for the national certification examination for the occupational therapy assistant administered by the <a href="https://www.nbcot.org/">National Board for Certification in Occupational Therapy</a> (NBCOT). After successful completion of this exam, the individual will be a Certified Occupational Therapy Assistant (COTA). In addition, most states require licensure in order to practice; however, state licenses are usually based on the results of the NBCOT Certification Examination. When you apply to sit for the certification exam, you will be asked to answer questions related to the topic of felony convictions. For further information on these limitations, contact NBCOT.  Note that a felony conviction may affect a graduate's ability to sit for the NBCOT certification examination or attain licensure. Connecticut requires a license in order to practice occupational therapy and the license is based on the results of the NBCOT Certification Examination.</p>

